const PLANET_POOLS: Record<PlanetKey, Record<SignKey, DomainPool>> = {
  Sun: SUN_SIGN_POOLS,
  Moon: MOON_SIGN_POOLS,
}

export function planetSignPool(
  planet: PlanetKey | string | undefined,
  sign: string | undefined,
  domain: PlanetSignDomain,
): readonly string[] {
  if (!planet || !sign) return []
  const planetEntry = PLANET_POOLS[planet as PlanetKey]
  if (!planetEntry) return []
  const signEntry = planetEntry[sign as SignKey]
  if (!signEntry) return []
  return signEntry[domain] ?? []
}
