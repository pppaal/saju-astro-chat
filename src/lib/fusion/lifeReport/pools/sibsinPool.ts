export function sibsinPool(
  sibsin: string | undefined,
  domain: SibsinDomain,
): readonly string[] {
  if (!sibsin) return []
  const entry = SIBSIN_POOLS[sibsin as SibsinKey]
  if (!entry) return []
  return entry[domain] ?? []
}

export function sibsinCategoryPool(
  category: string | undefined,
  domain: SibsinDomain,
): readonly string[] {
  if (!category) return []
  const entry = SIBSIN_CATEGORY_POOLS[category as SibsinCategory]
  if (!entry) return []
  return entry[domain] ?? []
}
