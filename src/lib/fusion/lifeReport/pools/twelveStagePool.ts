// Aliases — the codebase emits both '왕지'/'제왕' and '임관'/'건록' depending
// on which advanced engine produced the stage. Normalise so either form
// hits the same pool entry.
const STAGE_ALIASES: Record<string, StageKey> = {
  왕지: '제왕',
  임관: '건록',
}

/**
 * Return the variation pool array for a given (stage, domain) pair. The
 * caller passes the result into `pickVariation()` along with its own
 * seed keys.
 *
 * Returns an empty array (NOT undefined) so callers can spread/length
 * safely without nullish checks.
 */
export function twelveStagePool(
  stage: string | undefined,
  domain: TwelveStageDomain
): readonly string[] {
  if (!stage) return []
  const normalised = (STAGE_ALIASES[stage] ?? stage) as StageKey
  const entry = TWELVE_STAGE_POOLS[normalised]
  if (!entry) return []
  return entry[domain] ?? []
}
