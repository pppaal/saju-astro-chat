interface RelationLite {
  kind?: string
  detail?: string
}

/**
 * relation.detail에서 갑자 페어 추출.
 * "甲-庚 충" → "甲-庚"
 * "申-子-辰 합화수" → "申-子-辰"
 */
function extractPairKey(detail: string | undefined): string | null {
  if (!detail) return null
  const m = detail.match(/^([甲乙丙丁戊己庚辛壬癸子丑寅卯辰巳午未申酉戌亥]+(?:-[甲乙丙丁戊己庚辛壬癸子丑寅卯辰巳午未申酉戌亥]+)+)/)
  return m ? m[1] : null
}

/**
 * specific 페어로 KB 조회 (kind별 분기).
 */
function lookupPairKB(kind: string | undefined, pair: string): string | null {
  if (!kind) return null
  if (kind === '천간합') return CHEONGAN_HAP_KO[pair] || null
  if (kind === '천간충') return CHEONGAN_CHUNG_KO[pair] || null
  if (kind === '지지충') return JIJI_CHUNG_KO[pair] || null
  if (kind === '지지육합') return JIJI_YUKHAP_KO[pair] || null
  if (kind === '지지삼합') return JIJI_SAMHAP_KO[pair] || null
  if (kind === '지지형') return JIJI_SAMHYUNG_KO[pair] || null
  if (kind === '원진') return WONJIN_KO[pair] || null
  return null
}

/**
 * 본명 안의 갑자 동적 상호작용을 specific level로 풀어쓴 narration block.
 * 우선순위: 충/형 > 합 > 원진. top 3개까지.
 */
export function buildSajuDynamicsKo(
  relations: RelationLite[] | undefined
): string {
  if (!relations || relations.length === 0) return ''
  const PRIORITY = ['지지삼형', '천간충', '지지충', '지지삼합', '지지육합', '천간합', '지지방합', '원진']
  const sorted = [...relations].sort(
    (a, b) =>
      PRIORITY.indexOf(a.kind || '') - PRIORITY.indexOf(b.kind || '')
  )

  const lines: string[] = []
  const seen = new Set<string>()
  for (const r of sorted) {
    if (lines.length >= 3) break
    const pair = extractPairKey(r.detail)
    if (!pair) continue
    // 정규화된 페어키 (정렬해서 같은 pair 중복 방지)
    const normalized = pair.split('-').sort().join('-')
    const dedupeKey = `${r.kind}|${normalized}`
    if (seen.has(dedupeKey)) continue
    const kb = lookupPairKB(r.kind, pair)
    if (kb) {
      lines.push(`• ${kb}`)
      seen.add(dedupeKey)
    }
  }

  if (lines.length === 0) return ''
  return `### 본명 60갑자 동적 해석\n${lines.join('\n')}`
}
