// ============================================================

export function findSignCombination(
  pair: SignCombinationPair,
  sign1: ZodiacName,
  sign2: ZodiacName,
): SignCombination | null {
  return (
    SIGN_COMBINATIONS.find(
      (entry) => entry.pair === pair && entry.sign1 === sign1 && entry.sign2 === sign2,
    ) ?? null
  )
}

export function listSignCombinationsByPair(pair: SignCombinationPair): SignCombination[] {
  return SIGN_COMBINATIONS.filter((entry) => entry.pair === pair)
}
