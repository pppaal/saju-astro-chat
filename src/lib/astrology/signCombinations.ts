// their prose without re-deriving the meaning.
//
// Phase 1: Sun × Moon only — 12 × 12 = 144 entries.
// Venus × Mars, Sun × Ascendant, Moon × Ascendant will follow in later PRs.
//
// Tone:
//   - LifeReport 6th-pass naturalization. Zero jargon.
//   - 2-3 short sentences per language.
//   - Concrete and warm, not poetic or abstract.

